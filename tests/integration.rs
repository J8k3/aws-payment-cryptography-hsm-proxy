///   [34..66] dst key       32H  (mapped via key_mappings)
///   [66..68] max PIN len   2N
///   [68..70] src format    2N  "00"=ISO Format 0
///   [70..72] dst format    2N  "00"=ISO Format 0
///   [72..88] PIN block     16H  (encrypted under src ZPK)
///   [88..100] account      12N  (rightmost 12 PAN digits excl check)
