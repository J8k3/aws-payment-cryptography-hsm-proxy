// ── GW DUKPT MAC ─────────────────────────────────────────────────────────────

/// GW DUKPT Alg3 MAC round-trip using known BDK material.
///
/// GW payload layout:
/// ```text
/// mode(1) + infmt(1) + macsize(1) + algo(1) + pad(1)
/// + BDK (parse_bdk 32H) + KSN_desc(3H) + KSN(20H for TDES)
/// + msg_len(4H) + message_hex
/// verify appends: mac(8H)
/// ```
///
/// KSN descriptor "014": key_type=0, nibble_count=0x14=20 → 20H TDES KSN.
#[test]
#[ignore = "requires live proxy with known-material B0 BDK key in proxy.yaml"]
fn thales_gw_dukpt_alg3_mac_roundtrip() {
    let build_gw = |is_verify: bool, mac: &[u8]| {
        let mut p = Vec::new();
        p.push(if is_verify { b'1' } else { b'0' }); // mode
        p.push(b'1'); // infmt = hex
        p.push(b'0'); // macsize = 4 bytes
        p.push(b'3'); // algo = Alg3
        p.push(b'1'); // pad method (consumed)
        p.extend_from_slice(b"LTEST_B0BDK_0001LTEST_B0BDK_0001"); // BDK 32H (parse_bdk)
        p.extend_from_slice(b"014"); // KSN descriptor: nibbles=0x14=20 → TDES
        p.extend_from_slice(b"FFFF9876543210E00001"); // 20H TDES KSN
        p.extend_from_slice(b"0008"); // 8 bytes message
        p.extend_from_slice(b"AABBCCDD11223344"); // message hex
        if is_verify {
            p.extend_from_slice(mac);
        }
        p
    };

    // Generate
    let gen_resp = send_recv(&make_thales_frame(
        [0x00, 0x00],
        b"GW",
        &build_gw(false, &[]),
    ));
    let (ec, mac) = parse_thales_response(&gen_resp);
    assert_eq!(
        &ec,
        b"00",
        "GW DUKPT Alg3 generate failed: {}",
        String::from_utf8_lossy(&ec)
    );
    assert_eq!(mac.len(), 8, "GW mac_size=0 → 4 bytes → 8H");

    // Verify
    let ver_resp = send_recv(&make_thales_frame(
        [0x00, 0x00],
        b"GW",
        &build_gw(true, &mac),
    ));
    assert_ec00(&ver_resp, "GW DUKPT Alg3 verify");
}

// ── KQ ARQC Verify ───────────────────────────────────────────────────────────

/// KQ ARQC verify: confirms the proxy reaches APC (error 00 or ARQC mismatch,
/// never 68 "unrecognised command").
///
/// The E0 key has no known material (APC-generated), so the dummy ARQC will fail
/// cryptographic verification. We only assert the command is handled (not error 68).
///
/// KQ payload layout (binary fields after ASCII header):
///   mode(1) + scheme(1) + key_type(3H) + key(parse_legacy_key 16H)
///   + pan_seq(8B BCD: 6B PAN + 1B seq + 1B 0xFF pad)
///   + ATC(2B BE) + UN(4B) + txn_len(2B BE) + txn_data(n bytes)
///   + 0x3B delimiter + ARQC(8B)
#[test]
#[ignore = "requires live proxy with LTEST_E0IMK_0001 in proxy.yaml"]
fn thales_kq_arqc_verify_reaches_apc() {
    let mut payload = Vec::new();
    payload.push(b'0'); // mode 0 = verify ARQC only
    payload.push(b'1'); // scheme 1 = Mastercard M/Chip (Option A + Mastercard SKD)
    payload.extend_from_slice(b"000"); // key type 3H (consumed)
    payload.extend_from_slice(b"LTEST_E0IMK_0001"); // E0 AES-256 key 16H
                                                    // PAN/PSN: EMV pre-formatted, rightmost 16 of (PAN 4111111111111111 || PSN 01) = 1111111111111101
    payload.extend_from_slice(&[0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x01]);
    payload.extend_from_slice(&[0x00, 0x01]); // ATC = 0x0001
    payload.extend_from_slice(&[0x01, 0x02, 0x03, 0x04]); // UN
    payload.extend_from_slice(&[0x00, 0x10]); // txn_data length = 16 bytes
                                              // Transaction data (PDOL): 16 bytes of test data
    payload.extend_from_slice(&[
        0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x08, 0x40, 0x16, 0x05, 0x30, 0x00, 0x00, 0x00, 0x00,
        0x00,
    ]);
    payload.push(0x3B); // delimiter
                        // Dummy ARQC (will fail verification — that's expected for Level 1)
    payload.extend_from_slice(&[0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]);

    let resp = send_recv(&make_thales_frame([0x00, 0x00], b"KQ", &payload));
    assert_not_ec68(&resp, "KQ ARQC verify");
    // error 00 = valid ARQC (unlikely with dummy bytes)
    // any other non-68 code = handler reached APC (verification failed, as expected)
    let (ec, _) = parse_thales_response(&resp);
    println!("KQ response error code: {}", String::from_utf8_lossy(&ec));
}

